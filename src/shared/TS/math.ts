export default class MathUtils {
	static add(x: number, y: number): number {
		return x + y;
	}

	static subtract(x: number, y: number): number {
		return x - y;
	}

	static multiply(x: number, y: number): number {
		return x * y;
	}

	static divide(x: number, y: number): number {
		return x / y;
	}
}
